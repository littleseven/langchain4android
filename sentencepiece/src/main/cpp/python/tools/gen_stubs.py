"""Generates ``sentencepiece/__init__.pyi`` from the imported package.

The public Python surface of ``sentencepiece`` is assembled at *import time*
in ``sentencepiece.i`` (``setattr``, ``_add_snake_case``, ``Tokenize = Encode``
etc.), so a static parse of the source cannot see it. This script therefore
imports the built package and introspects the live classes/functions.

Two pieces of information are combined:

* The *method list* is discovered from the runtime objects, so the stub can
  never silently drift when a new public method (e.g. ``ParallelEncode``) or a
  new ``snake_case`` alias is added.
* The *types* come from a small curated overlay below. Anything discovered at
  runtime but missing from the overlay still gets a permissive
  ``(*args, **kwargs) -> Any`` stub (and is reported on stderr) so the stub
  stays complete and the build never breaks.

Usage::

    python tools/gen_stubs.py --output src/sentencepiece/__init__.pyi
    python tools/gen_stubs.py --check   # exit 1 if the committed stub is stale
"""

