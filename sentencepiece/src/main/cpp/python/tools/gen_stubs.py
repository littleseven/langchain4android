def to_snake(name):
  """Mirror of sentencepiece._add_snake_case for CamelCase -> snake_case."""
  return re.sub(r"(?<!^)(?=[A-Z])", "_", name).lower().replace("n_best", "nbest")


def build_sig_map(class_name):
  """Curated signatures expanded with their derived snake_case aliases."""
  sigs = {}
  for camel, sig in METHOD_SIGS.get(class_name, {}).items():
    sigs[camel] = sig
    if re.match(r"^[A-Z]+", camel):
      sigs[to_snake(camel)] = sig
  return sigs


def build_static_set(class_name):
  """Static method names for a class, including derived snake_case aliases."""
  statics = set()
  for cls_name, method in STATIC_METHODS:
    if cls_name != class_name:
      continue
    statics.add(method)
    if re.match(r"^[A-Z]+", method):
      statics.add(to_snake(method))
  return statics


def is_property(cls, name):
  return isinstance(inspect.getattr_static(cls, name, None), property)


def is_classmethod(cls, name):
  return isinstance(inspect.getattr_static(cls, name, None), classmethod)


def public_members(cls):
  """Public (and selected dunder) member names of a class, deduped/sorted."""
  members = set()
  for name in dir(cls):
    if name in SKIP_MEMBERS:
      continue
    if name.startswith("__"):
      if name not in ALLOWED_DUNDERS:
        continue
    elif name.startswith("_"):
      continue
    members.add(name)
  return members


def _method_sort_key(name):
  if name.startswith("__"):
    group = 0
  elif re.match(r"^[A-Z]", name):
    group = 1
  else:
    group = 2
  return (group, name)


def emit_method(name, sig, indent, static=False, is_cls=False):
  pad = " " * indent
  lines = []
  variants = sig if isinstance(sig, list) else [sig]
  overloaded = len(variants) > 1
  for variant in variants:
    if static:
      lines.append(f"{pad}@staticmethod")
    elif is_cls:
      lines.append(f"{pad}@classmethod")
    if overloaded:
      lines.append(f"{pad}@overload")
    lines.append(f"{pad}def {name}{variant}: ...")
  return lines


def emit_class(cls, class_name, uncurated):
  sig_map = build_sig_map(class_name)
  static_set = build_static_set(class_name)
  members = public_members(cls)

  properties = sorted(n for n in members if is_property(cls, n))
  methods = sorted((n for n in members if n not in properties), key=_method_sort_key)

  lines = [f"class {class_name}:"]
  body = []

  prop_types = PROPERTY_TYPES.get(class_name, {})
  for name in properties:
    typ = prop_types.get(name)
    if typ is None:
      uncurated.append(f"{class_name}.{name} (property)")
      typ = "Any"
    body.append("    @property")
    body.append(f"    def {name}(self) -> {typ}: ...")

  for name in methods:
    static = name in static_set
    is_cls = is_classmethod(cls, name)
    sig = sig_map.get(name)
    if sig is None:
      uncurated.append(f"{class_name}.{name}")
      if static:
        sig = "(*args: Any, **kwargs: Any) -> None" if name == "__init__" else "(*args: Any, **kwargs: Any) -> Any"
      elif is_cls:
        sig = "(cls, *args: Any, **kwargs: Any) -> Any"
      else:
        sig = "(self, *args: Any, **kwargs: Any) -> None" if name == "__init__" else "(self, *args: Any, **kwargs: Any) -> Any"
    body.extend(emit_method(name, sig, indent=4, static=static, is_cls=is_cls))

  if not body:
    body.append("    ...")
  lines.extend(body)
  return "\n".join(lines)


def discover_classes(module):
  found = {}
  for name in dir(module):
    if name.startswith("_"):
      continue
    obj = getattr(module, name)
    if isinstance(obj, type) and getattr(obj, "__module__", "").split('.')[0] == _MODULE_NAME:
      found[name] = obj
  return found


def discover_functions(module):
  found = {}
  for name in dir(module):
    if name.startswith("_"):
      continue
    obj = getattr(module, name)
    if isinstance(obj, type):
      continue
    if callable(obj) and getattr(obj, "__module__", "").split('.')[0] == _MODULE_NAME:
      found[name] = obj
  return found


def generate():
  uncurated = []
  classes = discover_classes(sentencepiece)
  functions = discover_functions(sentencepiece)

  ordered_names = [c for c in CLASS_ORDER if c in classes]
  ordered_names += sorted(c for c in classes if c not in CLASS_ORDER)

  blocks = [HEADER.rstrip("\n")]
  for name in ordered_names:
    blocks.append(emit_class(classes[name], name, uncurated))

  func_lines = []
  for name in sorted(functions, key=_method_sort_key):
    sig = FUNCTION_SIGS.get(name)
    if sig is None:
      camel = next(
          (c for c in FUNCTION_SIGS if to_snake(c) == name), None
      )
      sig = FUNCTION_SIGS.get(camel) if camel else None
    if sig is None:
      uncurated.append(name)
      sig = "(*args: Any, **kwargs: Any) -> Any"
    func_lines.append(f"def {name}{sig}: ...")
  if func_lines:
    blocks.append("\n".join(func_lines))

  text = "\n\n\n".join(blocks) + "\n"
  return text, uncurated


def main():
  parser = argparse.ArgumentParser(description=__doc__)
  parser.add_argument("--output", help="Write the stub to this path.")
  parser.add_argument(
      "--check",
      help="Compare against this existing stub and exit 1 if it is stale.",
  )
  args = parser.parse_args()

  text, uncurated = generate()

  if uncurated:
    sys.stderr.write(
        "gen_stubs: emitted permissive stubs for uncurated members "
        "(add typed signatures to METHOD_SIGS/PROPERTY_TYPES to improve):\n"
    )
    for name in uncurated:
      sys.stderr.write(f"  - {name}\n")

  if args.check:
    with open(args.check, encoding="utf-8") as f:
      current = f.read()
    if current != text:
      sys.stderr.write(
          f"\n{args.check} is out of date. Regenerate with:\n"
          "    python tools/gen_stubs.py --output "
          f"{args.check}\n"
      )
      return 1
    return 0

  if args.output:
    with open(args.output, "w", encoding="utf-8") as f:
      f.write(text)
  else:
    sys.stdout.write(text)
  return 0


if __name__ == "__main__":
  sys.exit(main())
